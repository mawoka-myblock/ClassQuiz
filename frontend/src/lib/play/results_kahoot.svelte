<script lang="ts">
    export let question_results: Array<{
        username: string;
        answer: string;
        right: boolean;
        time_taken: number;
        score: number;
        total_score: number;
    }>;

    export let username;
    let score_by_username = {} as Record<string, number>;
    let total_score_by_username = {} as Record<string, number>;
	for (const i of question_results) {
		score_by_username[i.username] = i.score;
		total_score_by_username[i.username] = i.total_score;
	}
</script>

<div>
	<div class="flex justify-center h-screen">
		<div class="m-auto flex flex-col">
			<p class="p-4 bg-black/40 rounded-lg text-2xl">
				+{score_by_username[username] ?? '0'}
			</p>
			<p>Total score: {total_score_by_username[username] ?? '0'}</p>
		</div>
	</div>
</div>
